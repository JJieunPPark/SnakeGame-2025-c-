Stage::Stage() : currentStage(0), map(), snake(3, &map, nullptr) {}

void Stage::setSnakeTeleport(Gate* g){
    snake.setGate(g);
}

void Stage::init(int stage) {
    currentStage = stage;
    map.initialize(stageData[stage - 1].map);
    snake.init(stageData[stage - 1].snake);
    map.initTimedWalls(
        stageData[stage - 1].timedWalls,
        stageData[stage - 1].timedWallPeriod
    );
    missions = stageData[stage - 1].mission;
}

void Stage::nextStage() {
    currentStage++;
    init(currentStage);
}

int Stage::getCurrentStage() const {
    return currentStage;
}

void Stage::drawMap() {
    map.draw();
}

void Stage::spawnItems() {
    map.spawnItems();
}

Map* Stage::getMap() {
    return &map;
}

Snake* Stage::getSnake() {
    return &snake;
}

bool Stage::checkMissions() {
    for (const auto& mission : missions) {
        if (!mission.condition(&map, &snake)) {
            return false;
        }
    }
    return true;
}

std::vector<MissionData> Stage::getMissions()const{
    return missions;
}

void Stage::getStage(int stage, Map &map, Snake &snake, std::vector<MissionData> &missionData) {
    if(stage < 1 || stage > N) throw "stage must be 1 to 4";
    map.initialize(stageData[stage-1].map); // 스테이지 데이터로 맵 초기화
    snake.init(stageData[stage-1].snake); // 스테이지 데이터로 스네이크 초기화
    map.initTimedWalls(
        stageData[stage-1].timedWalls,
        stageData[stage-1].timedWallPeriod
    );
    missionData = stageData[stage-1].mission;
}

